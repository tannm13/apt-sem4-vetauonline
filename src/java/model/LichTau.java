public class LichTau {
    private String tau;
    private String gaDung;
    private int sttGaDung;
    private String gioDi;
    private String gioDen;
    private int khoangCach;

    public String getTau() {
        return tau;
    }

    public void setTau(String tau) {
        this.tau = tau;
    }

    public String getGaDung() {
        return gaDung;
    }

    public void setGaDung(String gaDung) {
        this.gaDung = gaDung;
    }

    public int getSttGaDung() {
        return sttGaDung;
    }

    public void setSttGaDung(int sttGaDung) {
        this.sttGaDung = sttGaDung;
    }

    public String getGioDi() {
        return gioDi;
    }

    public void setGioDi(String gioDi) {
        this.gioDi = gioDi;
    }

    public String getGioDen() {
        return gioDen;
    }

    public void setGioDen(String gioDen) {
        this.gioDen = gioDen;
    }

    public int getKhoangCach() {
        return khoangCach;
    }

    public void setKhoangCach(int khoangCach) {
        this.khoangCach = khoangCach;
    }
    
    
}
