public class Ghe {
    private int noGhe;
    private String idGhe;
    private String loaiGhe;
    private int idTau;
    private String statusGhe;

    public int getNoGhe() {
        return noGhe;
    }

    public void setNoGhe(int noGhe) {
        this.noGhe = noGhe;
    }
    
    

    public String getIdGhe() {
        return idGhe;
    }

    public void setIdGhe(String idGhe) {
        this.idGhe = idGhe;
    }

    public String getLoaiGhe() {
        return loaiGhe;
    }

    public void setLoaiGhe(String loaiGhe) {
        this.loaiGhe = loaiGhe;
    }

    public int getIdTau() {
        return idTau;
    }

    public void setIdTau(int idTau) {
        this.idTau = idTau;
    }

    public String getStatusGhe() {
        return statusGhe;
    }

    public void setStatusGhe(String statusGhe) {
        this.statusGhe = statusGhe;
    }
    
    
}
